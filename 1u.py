r = float(input("Enter the radius of the circle :"))    # Getting input from user
pi = 3.14                                               # Initializing the value of PI
a = pi*r*r                                              # Finding the area of the circle
p = 2*pi*r                                              # Finding the perimeter of the circle

# Printing the area and perimeter of the circle
print("Area of the circle is :",a)
print("Perimeter of the circle is",p)